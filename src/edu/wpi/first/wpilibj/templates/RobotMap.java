package edu.wpi.first.wpilibj.templates;

/**
 * The RobotMap is a mapping from the ports sensors and actuators are wired into
 * to a variable name. This provides flexibility changing wiring, makes checking
 * the wiring easier and significantly reduces the number of magic numbers
 * floating around.
 */
public class RobotMap {
    public static final int LEFT_DRIVE_MOTOR_ALPHA = 2;
    public static final int LEFT_DRIVE_MOTOR_BETA = 5;    
    public static final int RIGHT_DRIVE_MOTOR_ALPHA = 4;
    public static final int RIGHT_DRIVE_MOTOR_BETA = 6;
    public static final int ARM_MOTOR = 3;
    public static final int CATAPULT_MOTOR = 7;    
    public static final String CAMERA_IP = "10.27.13.11";
}
